    private void btnPrikaziZakazaniPregledActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrikaziZakazaniPregledActionPerformed
        int red = tabelaZakazaniPregledi.getSelectedRow();
        if(red==-1){
            JOptionPane.showMessageDialog(this, "Niste izabrali nijedan red!","Greška",JOptionPane.ERROR_MESSAGE);
            return;
        }
        FormMode formMode;
        if(Kontroler.getInstanca().getUlogovani() instanceof Lekar){
            formMode = FormMode.FORM_VIEW;
        }
        else{
            formMode = FormMode.FORM_UPDATE;
        }
        ZakazaniPregledTabelaModel ztm = (ZakazaniPregledTabelaModel) tabelaZakazaniPregledi.getModel();
        ZakazaniPregled zp = ztm.vratiZakazaniPregled(red);
        ZakazivanjePregledaForma zpf = new ZakazivanjePregledaForma(this, true, formMode, zp);
        zpf.setVisible(true);       
    }//GEN-LAST:event_btnPrikaziZakazaniPregledActionPerformed

    private void btnObrisiZakazaniPregledActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiZakazaniPregledActionPerformed
        int red = tabelaZakazaniPregledi.getSelectedRow();
        if(red==-1){
            JOptionPane.showMessageDialog(this, "Niste izabrali nijedan red!","Greška",JOptionPane.ERROR_MESSAGE);
            return;
        }
        int odgovor = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da želite da obrišete zakazani pregled?");
        if(odgovor==JOptionPane.OK_OPTION){
            ZakazaniPregledTabelaModel ztm = (ZakazaniPregledTabelaModel) tabelaZakazaniPregledi.getModel();
            ZakazaniPregled zp = ztm.vratiZakazaniPregled(red);
            ServerskiOdgovor so = Kontroler.getInstanca().obrisiZakazaniPregled(zp);
            if(so.getResponseStatus()==ResponseStatus.ERROR){
                JOptionPane.showMessageDialog(this, so.getPoruka(), "Greška", JOptionPane.ERROR_MESSAGE);
            }
            else{
                JOptionPane.showMessageDialog(this, so.getPoruka(),"Poruka",JOptionPane.INFORMATION_MESSAGE);
                pretraga();
            }
        }
            
            }//GEN-LAST:event_btnObrisiZakazaniPregledActionPerformed

