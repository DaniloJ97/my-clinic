    private void srediTabelu() {
        ZakazaniPregledTabelaModel ztm = new ZakazaniPregledTabelaModel();
        tabelaZakazaniPregledi.setModel(ztm);
        pretraga();
        
    }

    public void pretraga() {
        String vrednost = txtPretragaZakazanihPregleda.getText();
        ZakazaniPregledTabelaModel ztm = (ZakazaniPregledTabelaModel) tabelaZakazaniPregledi.getModel();
        ZakazaniPregled zp = new ZakazaniPregled();
        Karton k = new Karton();
        k.setBrojKnjizice(vrednost);
        k.setIme(vrednost);
        k.setPrezime(vrednost);
        zp.setKarton(k);
        List<ZakazaniPregled> pregledi = Kontroler.getInstanca().vratiListuZakazanihPregleda(zp);
        ztm.setPregledi(pregledi);
    }

    private void srediDugmice() {
        if(Kontroler.getInstanca().getUlogovani() instanceof Lekar){
            btnObrisiZakazaniPregled.setVisible(false);
        }
    }

