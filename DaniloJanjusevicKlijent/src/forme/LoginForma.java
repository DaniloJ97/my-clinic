    private void btnPrijaviSeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrijaviSeActionPerformed
        if(!OpstaValidacija.getInstanca().daLiSuPoljaPopunjena(txtKorisnickoIme,txtKorisnickoIme)){
            JOptionPane.showMessageDialog(this, OpstaValidacija.getInstanca().getPoruka());
            return;
        }
        String korisnickoIme = txtKorisnickoIme.getText().trim();
        String lozinka = String.valueOf(passLozinka.getPassword()).trim();
        Korisnik korisnik;
        if(cbKorisnik.getSelectedItem().equals("Medicinska sestra")){
            System.out.println("Medicinska sestra");
            MedicinskaSestra medicinskaSestra = new MedicinskaSestra();
            medicinskaSestra.setKorisnickoIme(korisnickoIme);
            medicinskaSestra.setLozinka(lozinka);
            korisnik = medicinskaSestra;
        }
        else{
            System.out.println("Lekar");
            Lekar lekar = new Lekar();
            lekar.setKorisnickoIme(korisnickoIme);
            lekar.setLozinka(lozinka);
            korisnik = lekar;
        }
        boolean uspesno = Kontroler.getInstanca().prijaviKorisnika(korisnik);
        if(uspesno){
            GlavnaKlijentskaForma gkf = new GlavnaKlijentskaForma();
            gkf.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btnPrijaviSeActionPerformed

