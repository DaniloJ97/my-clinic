    private void popuniTabelu() {
        KartonTabelaModel ktm = new KartonTabelaModel();
        tabelaKartoni.setModel(ktm);
        pretraga();
    }

    public void pretraga() {
       String vrednost = txtImePrezimeLbo.getText().trim();
       Karton karton = new Karton(null, vrednost, vrednost,null, vrednost, null, null, null, null, null, null, null);
       List<Karton> kartoni = Kontroler.getInstanca().vratiListuKartona(karton);
       KartonTabelaModel ktm = (KartonTabelaModel) tabelaKartoni.getModel();
       ktm.setKartoni(kartoni);
    }

    private void postaviDugmice() {
        if(Kontroler.getInstanca().getUlogovani() instanceof Lekar){
           jButtonDeaktiviraj.setVisible(false);
        }
    }
