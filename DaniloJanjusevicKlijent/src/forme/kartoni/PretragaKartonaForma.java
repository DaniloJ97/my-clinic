    private void btnPrikaziKartonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrikaziKartonActionPerformed
        int red = tabelaKartoni.getSelectedRow();
        if(red==-1){
            JOptionPane.showMessageDialog(this, "Niste izabrali nijedan red!","Greška",JOptionPane.ERROR_MESSAGE);
            return;
        }
        FormMode formMode;
        if(Kontroler.getInstanca().getUlogovani() instanceof Lekar){
            formMode = FormMode.FORM_VIEW;
        }
        else{
            formMode = FormMode.FORM_UPDATE;
        }
        KartonTabelaModel ktm = (KartonTabelaModel) tabelaKartoni.getModel();
        Karton k = ktm.vratiKarton(red);
        DodavanjeKartonaForma dkf = new DodavanjeKartonaForma(this, true, formMode, k);
        dkf.setVisible(true);
    }//GEN-LAST:event_btnPrikaziKartonActionPerformed

    private void jButtonDeaktivirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeaktivirajActionPerformed
        int red = tabelaKartoni.getSelectedRow();
        if(red==-1){
            JOptionPane.showMessageDialog(this, "Niste izabrali nijedan red!","Greška",JOptionPane.ERROR_MESSAGE);
            return;
        }
        int odgovor = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da želite da obrišete karton?");
        if(odgovor==JOptionPane.OK_OPTION){
            KartonTabelaModel ktm = (KartonTabelaModel) tabelaKartoni.getModel();
            Karton k = ktm.vratiKarton(red);
            ServerskiOdgovor so = Kontroler.getInstanca().deaktivirajKarton(k);
            if(so.getResponseStatus()==ResponseStatus.ERROR){
                JOptionPane.showMessageDialog(this, so.getPoruka(), "Greška", JOptionPane.ERROR_MESSAGE);
            }
            else{
                JOptionPane.showMessageDialog(this, so.getPoruka(),"Poruka",JOptionPane.INFORMATION_MESSAGE);
                pretraga();
            }
        }
        
    }//GEN-LAST:event_jButtonDeaktivirajActionPerformed

